using System;
using System.Collections.Generic;
using System.Text;
using ApiViewer.Standard.Models;
using System.Linq;

namespace ApiViewer.Standard.Apis
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   An API pokemon characters. </summary>
    ///
    /// <remarks>   James Coates, 8/26/2017. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class ApiPokemonCharacters : Api
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the name. </summary>
        ///
        /// <value> The name. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override string Name { get { return "Pokemon Characters"; } }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the host. </summary>
        ///
        /// <value> The host. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override string Host { get { return "http://pokeapi.co"; } }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets URL of the document. </summary>
        ///
        /// <value> The URL. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override string Url { get { return "api/v2/pokemon/?limit=1000"; } }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the category. </summary>
        ///
        /// <value> The category. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override string Category { get { return "Games"; } }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets all. </summary>
        ///
        /// <remarks>   James Coates, 8/26/2017. </remarks>
        ///
        /// <returns>   all. </returns>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override List<IModel> GetAll()
        {
            if (_cache != null && _cache.Count > 0 && LastFetch.AddMinutes(10) > DateTime.Now) return _cache;
            var queryResult = GetResults<objectResponse>();

            List<IModel> data = new List<IModel>();

            if (queryResult != null && queryResult.Count > 0)
            {
                queryResult[0].results.ForEach(x => data.Add(x));

                //if (!string.IsNullOrEmpty(queryResult[0].next))
                //    queryResult = GetResults<objectResponse>(queryResult[0].next);
                //else
                //    queryResult = null;
            }
            
            LastFetch = DateTime.Now;

            data = data.OrderBy(o => o.Name).ToList();

            _cache = data;

            return data;
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   An object response. </summary>
        ///
        /// <remarks>   James Coates, 8/26/2017. </remarks>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        private class objectResponse
        {
            ////////////////////////////////////////////////////////////////////////////////////////////////////
            /// <summary>   Gets or sets the status. </summary>
            ///
            /// <value> The status. </value>
            ////////////////////////////////////////////////////////////////////////////////////////////////////

            public int count { get; set; }

            ////////////////////////////////////////////////////////////////////////////////////////////////////
            /// <summary>   Gets or sets the previous. </summary>
            ///
            /// <value> The previous. </value>
            ////////////////////////////////////////////////////////////////////////////////////////////////////

            public string previous { get; set; }

            public string next { get; set; }

            ////////////////////////////////////////////////////////////////////////////////////////////////////
            /// <summary>   Gets or sets the message. </summary>
            ///
            /// <value> The message. </value>
            ////////////////////////////////////////////////////////////////////////////////////////////////////

            public List<oPokemon> results { get; set; }
        }
    }
}
