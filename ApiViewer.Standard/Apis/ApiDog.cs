using System;
using System.Collections.Generic;
using System.Text;
using ApiViewer.Standard.Models;
using System.Linq;

namespace ApiViewer.Standard.Apis
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   An API dog. </summary>
    ///
    /// <remarks>   James Coates, 8/26/2017. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class ApiDog : Api
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the name. </summary>
        ///
        /// <value> The name. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override string Name { get { return "Dog API"; } }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the host. </summary>
        ///
        /// <value> The host. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override string Host { get { return "https://dog.ceo/api"; } }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets URL of the document. </summary>
        ///
        /// <value> The URL. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override string Url { get { return "breeds/list"; } }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the category. </summary>
        ///
        /// <value> The category. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override string Category { get { return "Animals"; } }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   An object response. </summary>
        ///
        /// <remarks>   James Coates, 8/26/2017. </remarks>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        private class objectResponse
        {
            ////////////////////////////////////////////////////////////////////////////////////////////////////
            /// <summary>   Gets or sets the status. </summary>
            ///
            /// <value> The status. </value>
            ////////////////////////////////////////////////////////////////////////////////////////////////////

            public string status { get; set; }

            ////////////////////////////////////////////////////////////////////////////////////////////////////
            /// <summary>   Gets or sets the message. </summary>
            ///
            /// <value> The message. </value>
            ////////////////////////////////////////////////////////////////////////////////////////////////////

            public List<string> message { get; set; }
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets all. </summary>
        ///
        /// <remarks>   James Coates, 8/26/2017. </remarks>
        ///
        /// <returns>   all. </returns>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override List<IModel> GetAll()
        {
            if (_cache != null && _cache.Count > 0 && LastFetch.AddMinutes(10) > DateTime.Now) return _cache;
            var queryResult = GetResults<objectResponse>();

            List<IModel> data = new List<IModel>();

            if (queryResult != null && queryResult.Count > 0 && queryResult[0].status == "success")
            {
                foreach (var value in queryResult[0].message)
                    data.Add(new oDog(value));
            }

            LastFetch = DateTime.Now;

            data = data.OrderBy(o => o.Name).ToList();

            _cache = data;

            return data;
        }

    }
}
